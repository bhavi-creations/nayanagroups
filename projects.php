    <script>
        const projectButtons = document.querySelectorAll(".projects_section_filter_btn");
        const projectItems = document.querySelectorAll(".projects_section_item");

        projectButtons.forEach(function(button) {
            button.addEventListener("click", function() {
                projectButtons.forEach(function(btn) {
                    btn.classList.remove("active");
                });

                this.classList.add("active");

                const filterValue = this.getAttribute("data-filter");

                projectItems.forEach(function(item) {
                    const itemCategory = item.getAttribute("data-category");

                    if (filterValue === "all" || itemCategory.includes(filterValue)) {
                        item.classList.remove("hide");
                    } else {
                        item.classList.add("hide");
                    }
                });
            });
        });
    </script>

